package ifConditions;

public class switchStatement {

	public static void main(String args[]) {

		char letter = 'A';

		switch (letter) {
		case 'A':
			System.out.println("Selected Car");
			break;
		case 'B':
			System.out.println("Selected Bike");
			break;
		case 'C':
			System.out.println("Selected Cycle");
		}
	}

}
